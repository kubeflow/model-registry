import typing as t
from pathlib import Path
from textwrap import dedent

def get_funcs(models: t.Iterable[Path]) -> t.Iterator[str]:
    for path in models:
        with path.open() as f:
            # skip boilerplate
            lines = iter(f.readlines())
            while not next(lines).startswith("package"):
                continue

            buf = []
            in_func = False
            in_import = False
            for raw in lines:
                line = raw.rstrip()
                if not line and not in_func:
                    continue

                if line.startswith("import "):
                    in_import = True
                    continue
                elif line == ")" and in_import:
                    in_import = False
                    buf.clear()
                    continue

                buf.append(line)
                if line.startswith("func"):
                    in_func = True
                elif line.startswith("}") and in_func:
                    in_func = False
                    yield "\n".join(buf)
                    buf.clear()
        path.unlink()


def get_name(func: str) -> str:
    # each func is declared as:
    # // comment
    # func funcName(args) error ...
    return func.split("\n")[1].split(" ")[1].split("(")[0]


